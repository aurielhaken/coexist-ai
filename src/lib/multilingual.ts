  if (/[\u4e00-\u9fff]/.test(text)) return 'zh'; // Chinois
  if (/[\u3040-\u309f\u30a0-\u30ff]/.test(text)) return 'ja'; // Japonais
  if (/[\u0900-\u097f]/.test(text)) return 'hi'; // Hindi
